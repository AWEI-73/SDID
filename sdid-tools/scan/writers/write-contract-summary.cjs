'use strict';

/**
 * write-contract-summary.cjs
 *
 * Writes .gems/docs/CONTRACT.md — human-readable summary derived from
 * apis.json + schemas.json (the scan-runner canonical products).
 *
 * This is a generated document; do not hand-edit.
 */

const fs   = require('fs');
const path = require('path');

/**
 * @param {object} apisJson    - result of writeApis()
 * @param {object} schemasJson - result of writeSchemas()
 * @param {string} outDir
 */
function writeContractSummary(apisJson, schemasJson, outDir) {
  const lines = [
    '# Contract Summary',
    '',
    `> Generated by scan-runner at ${new Date().toISOString()}`,
    `> Source: apis.json + schemas.json`,
    '',
  ];

  // ── APIs ──────────────────────────────────────────────────────────────────
  lines.push('## APIs', '');
  const apis = apisJson?.apis || [];
  if (apis.length === 0) {
    lines.push('_(no APIs found in contract)_', '');
  } else {
    for (const api of apis) {
      const storyTag = api.storyId ? ` · ${api.storyId}` : '';
      lines.push(`### ${api.apiName}${storyTag}`, '');
      if (api.methods?.length > 0) {
        lines.push('| Method | Params | Return |');
        lines.push('|--------|--------|--------|');
        for (const m of api.methods) {
          lines.push(`| \`${m.name}\` | \`${m.params || '—'}\` | \`${m.returnType || '—'}\` |`);
        }
        lines.push('');
      }
    }
  }

  // ── Schemas ───────────────────────────────────────────────────────────────
  lines.push('## Schemas', '');
  const schemas = schemasJson?.schemas || [];
  if (schemas.length === 0) {
    lines.push('_(no schemas found in contract)_', '');
  } else {
    for (const schema of schemas) {
      const storyTag = schema.storyId ? ` · ${schema.storyId}` : '';
      lines.push(`### ${schema.name} \`[${schema.kind}]\`${storyTag}`, '');
      if (schema.fields?.length > 0) {
        lines.push('| Field | Type | DB Type |');
        lines.push('|-------|------|---------|');
        for (const f of schema.fields) {
          lines.push(`| \`${f.name}\` | \`${f.type}\` | ${f.dbType || '—'} |`);
        }
        lines.push('');
      } else {
        lines.push('_(no fields)_', '');
      }
    }
  }

  if (!fs.existsSync(outDir)) fs.mkdirSync(outDir, { recursive: true });
  fs.writeFileSync(path.join(outDir, 'CONTRACT.md'), lines.join('\n'), 'utf8');
}

module.exports = { writeContractSummary };
