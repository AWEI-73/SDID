'use strict';

/**
 * write-scan-hub.cjs
 *
 * Writes .gems/docs/SCAN.md — human/AI entry point for all scan products.
 * Lists all outputs, their roles, and source-of-truth mapping.
 *
 * Generated last, after all other products exist.
 */

const fs   = require('fs');
const path = require('path');

/**
 * @param {object} opts
 * @param {string}   opts.projectName
 * @param {string}   opts.generatedAt  - ISO8601 timestamp for the run
 * @param {object}   opts.inputs       - { contract, draft, srcDirs }
 * @param {object}   opts.summary      - { fnCount, untagged, apiCount, schemaCount, moduleCount, edgeCount }
 * @param {string}   opts.outDir
 */
function writeScanHub({ projectName, generatedAt, inputs, summary, outDir }) {
  const s = summary || {};

  const lines = [
    '# SCAN Hub',
    '',
    `> Project: **${projectName}**`,
    `> Generated by scan-runner at ${generatedAt}`,
    '',
    '## Inputs',
    '',
    `| Input | Path |`,
    `|-------|------|`,
    `| Contract | ${inputs.contract || '_(not found)_'} |`,
    `| Draft | ${inputs.draft || '_(not found)_'} |`,
    `| Source | ${(inputs.srcDirs || []).join(', ') || '_(not found)_'} |`,
    '',
    '## Outputs',
    '',
    '| Artifact | Path | Role | Count |',
    '|----------|------|------|-------|',
    `| \`functions.json\` | \`.gems/docs/functions.json\` | VERIFY canonical input | ${s.fnCount ?? '?'} fns |`,
    `| \`apis.json\` | \`.gems/docs/apis.json\` | API surface (contract-derived) | ${s.apiCount ?? '?'} APIs |`,
    `| \`schemas.json\` | \`.gems/docs/schemas.json\` | Schema surface (contract-derived) | ${s.schemaCount ?? '?'} schemas |`,
    `| \`structure.json\` | \`.gems/docs/structure.json\` | Module structure (draft-derived) | ${s.moduleCount ?? '?'} modules |`,
    `| \`deps.json\` | \`.gems/docs/deps.json\` | Raw import/export graph (dep-scan) | — |`,
    `| \`deps-edges.json\` | \`.gems/docs/deps-edges.json\` | Curated cross-module edge projection | ${s.edgeCount ?? '?'} edges |`,
    `| \`CONTRACT.md\` | \`.gems/docs/CONTRACT.md\` | Human-readable contract summary | — |`,
    `| \`SCAN.md\` | \`.gems/docs/SCAN.md\` | This hub | — |`,
    '',
    '## Source-of-Truth Mapping',
    '',
    '| Output | Source | How |',
    '|--------|--------|-----|',
    '| `functions.json` | Codebase GEMS tags | scanV2() → mapToCanonical |',
    '| `apis.json` | `contract_iter-N.ts` | parseContract().apis |',
    '| `schemas.json` | `contract_iter-N.ts` | parseContract().entities + views + enums |',
    '| `structure.json` | `draft_iter-N.md` | draft moduleActions, with fallback to structure-ready action tables / Module Actions sections |',
    '| `deps.json` | Codebase imports | dep-scan raw graph (unmodified) |',
    '| `deps-edges.json` | `deps.json` | cross-module edge projection |',
    '| `CONTRACT.md` | `apis.json` + `schemas.json` | markdown summary |',
    '',
    '## Non-goals',
    '',
    '- Generated files are snapshots — do not hand-edit',
    '- Fix drift by updating raw sources or collector logic, not outputs',
    '- VERIFY only blocks on `functions.json`; other products are informational',
    '',
  ];

  if (!fs.existsSync(outDir)) fs.mkdirSync(outDir, { recursive: true });
  fs.writeFileSync(path.join(outDir, 'SCAN.md'), lines.join('\n'), 'utf8');
}

module.exports = { writeScanHub };
